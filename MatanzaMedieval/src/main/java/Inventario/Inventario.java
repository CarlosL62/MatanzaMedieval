    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        if (lblCombatiente1.getText().equals("Ninguno")) {
            try {
                jugablesPartida[0] = (Jugables) componentes.getPersonajesJugables().obtenerValor(jcbPersonajes.getSelectedIndex()).clone();
                jugablesPartida[0] = componentes.getPersonajesJugables().obtenerValor(jcbPersonajes.getSelectedIndex());
                lblCombatiente1.setText(jugablesPartida[0].getNickName());
                
                
                // Seteamos el item seleccionado al primero, luego de eliminar el item seleccionado
                //jcbPersonajes.removeItemAt(jcbPersonajes.getSelectedIndex());
                //jcbPersonajes.setSelectedIndex(1);
                //jcbPersonajesActionPerformed(evt);
            } catch (ListaException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (CloneNotSupportedException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                // Se verifica que no seleccione el mismo jugador dos veces
                jugablesPartida[1] = (Jugables) componentes.getPersonajesJugables().obtenerValor(jcbPersonajes.getSelectedIndex()).clone();
                jugablesPartida[1] = componentes.getPersonajesJugables().obtenerValor(jcbPersonajes.getSelectedIndex());
                if (jugablesPartida[0] == jugablesPartida[1]) {
                    //Advertencia
                    JOptionPane.showMessageDialog(this, "Asegurese de seleccionar combatientes diferentes", "Valor inválido", JOptionPane.INFORMATION_MESSAGE);
                    
                } else{
                    lblCombatiente2.setText(jugablesPartida[1].getNickName());
                    //jugablesPartida[1] = componentes.getPersonajesJugables().obtenerValor(jcbPersonajes.getSelectedIndex());
                    
                    //Se llama el formulario para la selección de dificultad
                    SeleccionDificultad seleccionDificultad = new SeleccionDificultad(componentes, jugablesPartida, opcionMapa, menu);
                    seleccionDificultad.setLocationRelativeTo(null);
                    seleccionDificultad.setVisible(true);
                    this.dispose();
                }
                
            } catch (ListaException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (CloneNotSupportedException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void setearLabelsObjetos() {
        try {
            lblObjeto.setText(componentes.getObjetosInventario().obtenerValor(objetoSeleccionado).getNombreObjeto());
            lblCantidadObjetos.setText(String.valueOf(componentes.getObjetosInventario().obtenerValor(objetoSeleccionado).getCantidadDisponible()));
            lblDescripcionObjeto.setText(componentes.getObjetosInventario().obtenerValor(objetoSeleccionado).getDescripcion());
            lblPrecioObjeto.setText(String.valueOf(componentes.getObjetosInventario().obtenerValor(objetoSeleccionado).getPrecio()));
            
            //ImageIcon imagenIcono = new ImageIcon("src\\main\\java\\imagenes\\" + jugable.getNombre().toLowerCase() + ".png");
            ImageIcon imagenIcono = new ImageIcon(getClass().getResource(componentes.getObjetosInventario().obtenerValor(objetoSeleccionado).getRuta()));
            Image imagen = imagenIcono.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
            ImageIcon imagenFinal = new ImageIcon(imagen);
            lblImagenObjeto.setIcon(imagenFinal);
        } catch (ListaException ex) {
            Logger.getLogger(Tienda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
