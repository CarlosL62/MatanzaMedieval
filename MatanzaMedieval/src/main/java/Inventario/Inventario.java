    private void actualizarOro(){
        lblOro.setText(String.valueOf(componentes.getOro()));
    }
    
    //Método para obtener los personajes en inventario
    private void personajesEnInventario(){
        jcbPersonajes.removeAllItems();
        for (int i = 0; i < componentes.getPersonajesJugables().obtenerSize(); i++) {
            try {
                jcbPersonajes.addItem(componentes.getPersonajesJugables().obtenerValor(i).getNickName());
            } catch (ListaException ex) {
                Logger.getLogger(Tienda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
